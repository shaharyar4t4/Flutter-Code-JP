  print("-------------------------------------------------");
  print('my output is ${num1 + num2}');
  print('my output is ${num1 - num2}');
  print('my output is ${num1 * num2}');
  print('my output is ${num1 / num2}');
  print('my output is ${num1 % num2}');
  print('my output is ${num1 ~/ num2}');
  print("-------------------------------------------------");

  // used all  comparison operator
  print("-------------------------------------------------");
  print('my output is ${num1 == num2}');
  print('my output is ${num1 != num2}');
  print('my output is ${num1 > num2}');
  print('my output is ${num1 < num2}');
  print('my output is ${num1 >= num2}');
  print('my output is ${num1 <= num2}');
  print("-------------------------------------------------");

  // used all  logical operator
  print("-------------------------------------------------");
  print('my output is ${num1 > num2 && num1 < num2}');
  print('my output is ${num1 > num2 || num1 < num2}');
  print('my output is ${!(num1 > num2)}');
  print("-------------------------------------------------");
}
